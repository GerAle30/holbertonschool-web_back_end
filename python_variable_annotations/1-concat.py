#!/usr/bin/env python3
"""
This module defines a function that concatenates two strings.
"""

def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings.

    Args:
        str1 (str): First string.
        str2 (str): Second string.

    Returns:
        str: The result of str1 + str2.
    """
    return str1 + str2

