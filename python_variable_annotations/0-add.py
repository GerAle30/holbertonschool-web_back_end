#!/usr/bin/env python3
"""
This module defines a function that adds two float numbers.
"""

def add(a: float, b: float) -> float:
    """
    Returns the sum of two float numbers.

    Args:
        a (float): First number to add.
        b (float): Second number to add.

    Returns:
        float: The result of a + b.
    """
    return a + b

